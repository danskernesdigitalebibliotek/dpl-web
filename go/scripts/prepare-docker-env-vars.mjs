/**
 * @file
 *
 * Build-time script to prepare environment variables for Docker builds.
 */
import { writeFileSync } from "node:fs"
import { join } from "node:path"
import { env } from "node:process"

const CLUSTER_HOSTNAME = "dplplat02.dpl.reload.dk"
const ENV_FILENAME = ".env.local"

function prepareDockerEnvVars() {
  let envFileContent = generateEnvFileContents()

  if (envFileContent) {
    // Remove leading whitespace from each line before writing.
    const finalContent = envFileContent.replace(/^ +/gm, "")
    const envFilePath = join(import.meta.dirname, "..", ENV_FILENAME)
    console.info(`Writing '${envFilePath}'…`, finalContent)
    writeFileSync(envFilePath, finalContent, { flag: "a", flush: true })
  }
}

function generateEnvFileContents() {
  const lagoonEnvironment = env.LAGOON_ENVIRONMENT
  const lagoonProject = env.LAGOON_PROJECT
  // LAGOON_ROUTE should point to the main CMS route.
  const lagoonRoute = env.LAGOON_ROUTE
  // LAGOON_ROUTES contains all the routes, comma-separated in no logical order.
  const lagoonRoutes = env.LAGOON_ROUTES

  if (env.DPL_CMS_BASE_URL && env.DPL_GO_BASE_URL) {
    // Best case, the variables are already defined in the environment, bake
    // them into the env-file with no changes.
    return `
      # Autogenerated by prepare-docker-env-vars.mjs (predefined values).
      DPL_CMS_BASE_URL=${env.DPL_GO_BASE_URL}
      DPL_GO_BASE_URL=${env.DPL_GO_BASE_URL}
    `
  } else if (lagoonRoute && lagoonRoutes) {
    // Second best case, look through the list of Lagoon routes to find the hostnames we need.
    const routes = lagoonRoutes.split(",")

    // Production URLs start with `go.`, (autogenerated) test URLs start with `node.`.
    const goURL = findMatchingRoute(routes, ["https://go.", "https://www.go.", "https://node."])

    if (!goURL) {
      throw new Error("URL for Go! not found in LAGOON_ROUTES, cannot continue.")
    }
    return `
      # Autogenerated by prepare-docker-env-vars.mjs (LAGOON_ROUTES based).
      DPL_CMS_BASE_URL=${lagoonRoute}
      DPL_GO_BASE_URL=${goURL}
    `
  } else if (lagoonEnvironment && lagoonProject) {
    // Fallback, guess hostnames from Lagoon environment (used for PR environments).
    const baseHostname = `${lagoonEnvironment}.${lagoonProject}.${CLUSTER_HOSTNAME}`

    return `
      # Autogenerated by prepare-docker-env-vars.mjs (Lagoon-env based).
      DPL_CMS_BASE_URL=https://varnish.${baseHostname}
      DPL_GO_BASE_URL=https://node.${baseHostname}
    `
  } else {
    // In case we cannot determine the proper URLs, this happens at build time.
    // For this case, we just use a fallback value.
    return `
      # Autogenerated by prepare-docker-env-vars.mjs (fallback).
      DPL_CMS_BASE_URL=http://dpl-cms.test
      DPL_GO_BASE_URL=http://dpl-go.test
    `
  }
}

/**
 * Given a list of routes, go through the prefixes in order and find the first one that matches..

 * @param {string[]} routes
 * @param {string[]} prefixes
 * @returns string | undefined
 */
export function findMatchingRoute(routes, prefixes) {
  for (const prefix of prefixes) {
    const match = routes.find(route => route.startsWith(prefix))

    if (match) {
      return match
    }
  }
}

if (import.meta.main) {
  prepareDockerEnvVars()
}
